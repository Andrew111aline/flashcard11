export function getTranslation(lang: string, key: string, ...args: any[]): string {
  const dict = LANGS[lang] || LANGS['zh'];
  const value = dict[key];

  if (value === undefined) {
    const fallback = LANGS['zh'][key];
    if (fallback === undefined) return key;
    return typeof fallback === 'function' ? fallback(...args) : fallback;
  }

  return typeof value === 'function' ? value(...args) : value;
}

export function formatNumber(n: number, lang: string) {
  return new Intl.NumberFormat(lang === 'zh' ? 'zh-CN' : 'en-US').format(n);
}

export function formatPercent(ratio: number, lang: string) {
  return new Intl.NumberFormat(
    lang === 'zh' ? 'zh-CN' : 'en-US',
    { style: 'percent', maximumFractionDigits: 1 }
  ).format(ratio);
}

export function formatDate(isoStr: string, lang: string) {
  return new Date(isoStr).toLocaleDateString(
    lang === 'zh' ? 'zh-CN' : 'en-US',
    { month: 'short', day: 'numeric' }
  );
}
